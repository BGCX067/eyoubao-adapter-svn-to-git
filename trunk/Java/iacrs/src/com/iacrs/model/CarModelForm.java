package com.iacrs.model;

public class CarModelForm
{
    private Integer id;
    
    private String name;
    
    private String category;
    
    private String brand;
    
    private String style;
    
    private Integer coachType;
    
    private Integer gearType;
    
    private String sweptVolume;
    
    private String imagePath;
    
    private Integer dailyRental;
    
    private Integer dailyPremium;
    
    private Integer preauth;
    
    public Integer getId()
    {
        return id;
    }
    
    public void setId(Integer id)
    {
        this.id = id;
    }
    
    public String getName()
    {
        return name;
    }
    
    public void setName(String name)
    {
        this.name = name;
    }
    
    public String getCategory()
    {
        return category;
    }
    
    public void setCategory(String category)
    {
        this.category = category;
    }
    
    public String getBrand()
    {
        return brand;
    }
    
    public void setBrand(String brand)
    {
        this.brand = brand;
    }
    
    public String getStyle()
    {
        return style;
    }
    
    public void setStyle(String style)
    {
        this.style = style;
    }
    
    public Integer getCoachType()
    {
        return coachType;
    }
    
    public void setCoachType(Integer coachType)
    {
        this.coachType = coachType;
    }
    
    public Integer getGearType()
    {
        return gearType;
    }
    
    public void setGearType(Integer gearType)
    {
        this.gearType = gearType;
    }
    
    public String getSweptVolume()
    {
        return sweptVolume;
    }
    
    public void setSweptVolume(String sweptVolume)
    {
        this.sweptVolume = sweptVolume;
    }
    
    public String getImagePath()
    {
        return imagePath;
    }
    
    public void setImagePath(String imagePath)
    {
        this.imagePath = imagePath;
    }
    
    public Integer getDailyRental()
    {
        return dailyRental;
    }
    
    public void setDailyRental(Integer dailyRental)
    {
        this.dailyRental = dailyRental;
    }
    
    public Integer getDailyPremium()
    {
        return dailyPremium;
    }
    
    public void setDailyPremium(Integer dailyPremium)
    {
        this.dailyPremium = dailyPremium;
    }
    
    public Integer getPreauth()
    {
        return preauth;
    }
    
    public void setPreauth(Integer preauth)
    {
        this.preauth = preauth;
    }
}
